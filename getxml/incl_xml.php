<?

function createXMLHeader($feedName) {
	return "<?xml version='1.0' encoding='UTF-8'?>\n";
}

function createXMLTags($key, $value) {
	return "<" . $key . ">" . $value . "</" . $key . ">\n";
}

function createOpenXMLTagA($key, $aKey, $aValue) {
	return "<" . $key . " " . $aKey . "=\"" . $aValue . "\">\n";
}

function createOpenXMLTag($key) {
	return "<" . $key . ">";
}

function createCloseXMLTag($key) {
	return "</" . $key . ">\n";
}

function xmlEncode($string) {
	return "<![CDATA[".$string."]]>";
}

?>